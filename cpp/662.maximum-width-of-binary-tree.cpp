/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
public:
    int widthOfBinaryTree(TreeNode* root) {
      vector<TreeNode*> curLevel, nextLevel;
      curLevel.push_back(root);

      int res = 0;
      while (!curLevel.empty()) {
        int curWidth = 0;
        int i = 0;
        int j = curLevel.size() - 1;
        while (i <= j && (curLevel[i] == nullptr || curLevel[j] == nullptr)) {
          if (curLevel[i] == nullptr) {
            i++;
          }

          if (curLevel[j] == nullptr) {
            j--;
          }
        }

        if (i > j) break;
        res = max(res, j - i + 1);

        for (int x = i; x <= j; ++x) {
          if (curLevel[x] != nullptr) {
            nextLevel.push_back(curLevel[x]->left);
            nextLevel.push_back(curLevel[x]->right);
          } else {
            nextLevel.push_back(nullptr);
            nextLevel.push_back(nullptr);
          }
        }

        curLevel.clear();
        swap(curLevel, nextLevel);
      }

      return res;
    }
};
