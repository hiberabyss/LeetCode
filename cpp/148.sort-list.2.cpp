/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
  ListNode* mergeList(ListNode* l1, ListNode* l2) {
    ListNode l;

    auto* cur = &l;
    while (l1 != nullptr && l2 != nullptr) {
      if (l1->val < l2->val) {
        cur->next = l1;
        l1 = l1->next;
      } else {
        cur->next = l2;
        l2 = l2->next;
      }
      cur = cur->next;
    }
    
    if (l1 == nullptr) cur->next = l2;
    else if (l2 == nullptr) cur->next = l1;

    return l.next;
  }

    ListNode* sortList(ListNode* head) {
      if (head == nullptr) return nullptr;
      auto* slow = head;
      auto* fast = head->next;
      if (fast == nullptr) {
        return head;
      }

      while (fast != nullptr && fast->next != nullptr) {
        slow = slow->next;
        fast = fast->next->next;
      }

      auto* l2 = slow->next;
      slow->next = nullptr;
      head = sortList(head);
      l2 = sortList(l2);
      return mergeList(head, l2);
    }
};
